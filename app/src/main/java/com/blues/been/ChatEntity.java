public class ChatEntity {
    private int pic_chat;
    private String title_chat;
    private String context_chat;
    private String time_chat;

    public int getPic_chat() {
        return pic_chat;
    }

    public void setPic_chat(int pic_chat) {
        this.pic_chat = pic_chat;
    }

    public String getTitle_chat() {
        return title_chat;
    }

    public void setTitle_chat(String title_chat) {
        this.title_chat = title_chat;
    }

    public String getContext_chat() {
        return context_chat;
    }

    public void setContext_chat(String context_chat) {
        this.context_chat = context_chat;
    }

    public String getTime_chat() {
        return time_chat;
    }

    public void setTime_chat(String time_chat) {
        this.time_chat = time_chat;
    }
}
